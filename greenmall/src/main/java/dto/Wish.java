package dto;

public class Wish {
	private int wishKey;
	private int pNo;
	private String mId;
	
	
	public int getWishKey() {
		return wishKey;
	}
	public void setWishKey(int wishKey) {
		this.wishKey = wishKey;
	}
	public int getpNo() {
		return pNo;
	}
	public void setpNo(int pNo) {
		this.pNo = pNo;
	}
	public String getmId() {
		return mId;
	}
	public void setmId(String mId) {
		this.mId = mId;
	}

}
