package dto;

public class Category {
	private int cNum;
	private int cName;
	
	
	public int getCategoryNum() {
		return cNum;
	}
	public void setCategoryNum(int categoryNum) {
		this.cNum = categoryNum;
	}
	public int getcName() {
		return cName;
	}
	public void setcName(int cName) {
		this.cName = cName;
	}

}
